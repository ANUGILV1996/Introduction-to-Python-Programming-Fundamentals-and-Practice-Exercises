# Data type -Set.

numbers = {20, 10, 40, 60, 90}
print(numbers)

# Add numbers
numbers.add(78)
print(numbers)

# To remove numbers
numbers.remove(10)
print(numbers)

# Union Operation
set_a = {1, 2, 3, 4, 5}
set_b = {4, 5, 6, 7, 8}
print(set_a | set_b)

# Intersection Operation
set_a = {1, 2, 3, 4, 5}
set_b = {4, 5, 6, 7, 8}
print(set_a & set_b)

# Difference Operation
set_a = {1, 2, 3, 4, 5}
set_b = {4, 5, 6, 7, 8}
print(set_a - set_b)
print(set_b - set_a)

