# For loop - Part 1

# For loop in String
# problem 1:
for i in 'inmakes':
    print(i)

# Problem 2:
obj = 'Inmakes'
for i in obj:
    print(i)

# For loop in List
# Problem 3:
obj = [1, 2, 3, 6, 5]
for i in obj:
    print(i)

# For loop in Range (0, 9)
# Problem 4:
for i in range(10):
    print(i)

# For loop in Range (10, 20)
# Problem 5:
for i in range(10,20):
    print(i)

# For loop in Range with 2 increment (10, 20, 2)
# Problem 6:
for i in range(10, 20, 2):
    print(i)

