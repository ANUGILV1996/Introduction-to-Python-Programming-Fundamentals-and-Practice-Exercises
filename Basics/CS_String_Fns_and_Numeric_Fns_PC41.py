# String Functions and Numeric Functions.
new_string = 'hello i am Inmakes'

# Upper case string functions.
print(new_string.upper())

# Lower case string functions.
print(new_string.lower())

# Starting with which letter?
print(new_string.startswith('h'))

# Ends with which letter?
print(new_string.endswith('s'))

# Minimum vale.
print(min(2, 4, 1, 6, 7))

# Maximum  vale.
print(max(22, 44, 11, 33, 123))

# Absolute  vale.
print(abs(-788))

